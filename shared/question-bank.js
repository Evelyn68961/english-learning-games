// Return themes filtered for a learner level.
//   forLevel('elementary') -> only elementary grammar (Grades 3-6 friendly).
//   forLevel('junior')     -> all grammar (junior includes elementary content).
//   forLevel()             -> raw themes, no filtering.
window.QUESTION_BANK.forLevel = function (level) {
    if (level !== 'elementary' && level !== 'junior') return this.themes;
    const allowed = level === 'junior'
        ? new Set(['elementary', 'junior'])
        : new Set(['elementary']);
    return this.themes.map(t => ({
        ...t,
        grammar: t.grammar.filter(g => allowed.has(g.level || 'junior')),
    }));
};
